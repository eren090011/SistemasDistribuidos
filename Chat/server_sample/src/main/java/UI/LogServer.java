   // agregar un evento al lod
    public void appendLog(final String message) {
        
        // Asegura que el c√≥digo se ejecute en el hilo de eventos de Swing
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                txtArea.append(message + "\n");
            }
        });
    }
    // agregar un usuario a la lista de usuarios
     public void addUser(String userName) {
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                listModel.addElement(userName);
            }
        });
    }

    // eliminar un usuario de la lista de usuarios
    public void removeUser(String userName) {
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                int index = listModel.indexOf(userName);
                if (index != -1) {
                    listModel.remove(index);
                }
            }
        });      
    }


    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        String tipo=" ";
        switch (this.operacion) {
            case "Sacar dinero debito":
                // se hara el procedimiento para sacar dinero a deb
               // por el momento hasta que no se conecten las cosas ira directamente al recibo
                tipo ="debito";
                Recibo reciboDeb =new Recibo(this.id,this.operacion,tipo);
                reciboDeb.setVisible(true);
                reciboDeb.setLocationRelativeTo(null);
                this.setVisible(false);
                break;
            case "Consignar dinero":
                // se preguntara a donde consignar dinero si a cred o a deb y luego si se consignara
                ConsignarEn consEn = new ConsignarEn(this.id,"Consignar dinero");
                consEn.setVisible(true);
                consEn.setLocationRelativeTo(null);
                this.setVisible(false);
                break;
            case "Hacer avance":
                // se hara el procedimiento para sacar dinero a cred
                 // por el momento hasta que no se conecten las cosas ira directamente al recibo
                tipo ="credito";
                Recibo reciboCred =new Recibo(this.id,this.operacion,tipo);
                reciboCred.setVisible(true);
                reciboCred.setLocationRelativeTo(null);
                this.setVisible(false);
                break;
            default:
                // en caso de que pase algo raro
                OperacionInvalida opInvalida =new OperacionInvalida(this.id,"Operaci√≥n invalida");
                opInvalida.setVisible(true);
                opInvalida.setLocationRelativeTo(null);
                this.setVisible(false);
                break;
 }
        
    }//GEN-LAST:event_btnContinuarActionPerformed

